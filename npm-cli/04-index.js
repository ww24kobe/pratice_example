const prompts = require('prompts');
prompts.override(require('yargs').argv);

(async () => {
    const response = await prompts([{
            type: 'text',
            name: 'twitter',
            message: `What's your twitter handle?`
        },
        {
            type: 'multiselect',
            name: 'color',
            message: 'Pick colors',
            choices: [{
                    title: 'Red',
                    value: '#ff0000'
                },
                {
                    title: 'Green',
                    value: '#00ff00'
                },
                {
                    title: 'Blue',
                    value: '#0000ff'
                },
                {
                    title: 'pink',
                    value: '粉色呀'
                }
            ],
        }
    ]);

    console.log(response);
})();